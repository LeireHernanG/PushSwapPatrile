#include <stdio.h>

int	ft_sa(int *a)
{
	int	i;
	int	*temp;

	temp = NULL;
	i = 0;
	if (!a)
		return (0);
	temp[i] = a[i];
	a[i] = a[i + 1];
	a[i] = temp[i];
	return (*a);
}
